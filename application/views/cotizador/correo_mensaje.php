function formatPrice($price, $min = 10, $dec = '.', $thousand = ',') {
    $r = number_format($price, 2, $dec, $thousand);
    $round = explode($dec, $r);

    $entero = $round[0];
    if($thousand != '')
        $entero = str_replace($thousand, '', $entero);
    $fraccion = $round[1];

    for ($i = 0; $i <= 100; $i = $i + $min) {
        if ($i < $fraccion && $i + $min > $fraccion) {
            if (($i + $min - $fraccion) <= ($fraccion - $i))
                $fraccion = $i + $min;
            else if (($i + $min - $fraccion) > ($fraccion - $i))
                $fraccion = $i;

            if ($fraccion == 100) {
                $fraccion = 0;
                $entero = $entero + 1;
            }
        }
    }
    $result = number_format($entero.$dec.$fraccion, 2, $dec, $thousand);
    return $result;
}

function sort_object($obj, $predicate)
{
    for ($i = 1; $i < count($obj); $i++) {
        for ($j = 0; $j < count($obj) - $i; $j++) {
            if ($predicate($obj[$j], $obj[$j + 1])) {
                $k = $obj[$j + 1];
                $obj[$j + 1] = $obj[$j];
                $obj[$j] = $k;
            }
        }
    }
    return $obj;
}

