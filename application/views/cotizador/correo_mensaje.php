function subfijo($xx)
{ // esta funci�n regresa un subfijo para la cifra
    $xx = trim($xx);
    $xstrlen = strlen($xx);
    if ($xstrlen == 1 || $xstrlen == 2 || $xstrlen == 3)
        $xsub = "";
    //
    if ($xstrlen == 4 || $xstrlen == 5 || $xstrlen == 6)
        $xsub = "MIL";
    //
    return $xsub;
}

function divide_in($number, $parts)
{
    $max_div = 1;
    if ($number >= $parts)
        $max_div = intval($number / $parts);
    for ($i = 0; $i < $parts; $i++) {
        $number -= $max_div;
        $result[$i] = $max_div;
        if ($i == $parts - 1)
            $result[$i] += $number;
    }

    return $result;
}

